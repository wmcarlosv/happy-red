Route::get('storage/payment/{id}/{filename}', function ($id,$filename)
    {
        $path = storage_path('app/payment/'.$id.'/'. $filename);
        if (!File::exists($path)) {
            abort(404);
        }
    
        $file = File::get($path);
        $type = File::mimeType($path);
    
        $response = Response::make($file, 200);
        $response->header("Content-Type", $type);
    
        return $response;
    });